package com.example.tarmoapp;

public class FitnessTip {
    private String name, text;

    public FitnessTip(String name, String text) {
        this.name = name;
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public String getText() {
        return text;
    }

    public String toString() {
        return name;
    }
}
